static bool Reset(dsa::vent::tower::dbnf::LengthString value, dsa::vent::tower::dbnf::LengthString& index)
{
    index = value;
    return false;
}

static bool ClearNodes(std::list<dsa::vent::tower::dbnf::Node*>& nodes)
{
    while(!nodes.empty())
    {
        dsa::vent::tower::dbnf::Node* node = nodes.front();
        nodes.pop_front();
        delete node;
    }

    return true;
}

